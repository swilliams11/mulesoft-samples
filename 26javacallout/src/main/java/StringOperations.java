
public class StringOperations {
	/**
	 * Combines two strings.
	 * @param a
	 * @param b
	 * @return
	 */
	public static String concat(String a, String b) {
		return a + " " + b;
	}
	
	public String multiConcat(String a, String b, String c, String d) {
		return a + " " + b + " " + c + " " + d;
	}
}
